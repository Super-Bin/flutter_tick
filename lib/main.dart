  final String title;

  @override
  _MyHomePageState createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {


  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text("Loading"),
      ),
      body: Container(
        child: Column(
          children: [
            MaterialButton(
              color: Colors.blue,
              textColor: Colors.white,
              child: new Text('开始'),
              onPressed: _onClickStart,
            ),
            TickSuccessView(key: tickSuccessViewKey,),
            Text("测试自定义组件的高度位置"),
          ],
        ),
      ),
    );
  }

  void _onClickStart() {
    //父组件中调用
    tickSuccessViewKey.currentState.startAnimation();
  }
